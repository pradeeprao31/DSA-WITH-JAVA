package pradeep;
import java.util.*;
public class string_manipulation {

	public static void main(String[] args) {
		String str= "the army is good to secure are country ";
		
		System.out.println(str.isEmpty());  // false;
		
		
		System.out.println(str.length()); // 39
		
		System.out.println(str.charAt(5)); // r
		
		System.out.println(str.indexOf("a",8)); // 27
		
		
		System.out.print(str.indexOf("g"));
		
		
	}

}
